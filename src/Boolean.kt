fun main() {
    //Ini adalah tipe data Boolean, hanya ada 2, yaitu "true" dan "false"
    var SayaAdalahManusia: Boolean = true
    var SayaAdalahRobot: Boolean = false

    println(SayaAdalahManusia)
    println(SayaAdalahRobot)

    // Untuk menerjemahkan "true" dan "false" dengan "benar" dan "salah", gunakan kode dibawah ini
    if (SayaAdalahManusia) {
        println("Benar")
    }else{
        println("Salah")
    }

    if (SayaAdalahRobot) {
        println("Benar")
    }else{
        println("Salah")
    }

}